public class Supermarket {

	public String supermarketName, section;
	public int choiceMenu, choiceOptions, storage;

	// Seção do Mercado

	public String getSupermarketName() {
		return supermarketName;
	}

	public void setSupermarketName(String supermarketName) {
		this.supermarketName = supermarketName;
	}

	public String getSection() {
		return section;
	}

	public void setSection(String section) {
		this.section = section;
	}

	public int getChoiceMenu() {
		return this.choiceMenu;
	}

	public void setChoiceMenu(int choice) {
		this.choiceMenu = choice;
	}

	public int getChoiceOptions() {
		return choiceOptions;
	}

	public void setChoiceOptions(int choiceOptions) {
		this.choiceOptions = choiceOptions;
	}

	public int getStorage() {
		return storage;
	}

	public void setStorage(int storage) {
		this.storage = storage;
	}
	
	

}
